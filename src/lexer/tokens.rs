use super::TokenKind;

pub fn token_name(kind: &TokenKind) -> &'static str {
    match kind {
        TokenKind::Func => "func",
        TokenKind::Export => "export",
        TokenKind::Return => "return",
        TokenKind::Let => "let",
        TokenKind::Mut => "mut",
        TokenKind::If => "if",
        TokenKind::Else => "else",
        TokenKind::While => "while",
        TokenKind::True => "true",
        TokenKind::False => "false",
        TokenKind::Struct => "struct",
        TokenKind::Restrict => "restrict",
        TokenKind::I32 => "i32",
        TokenKind::I64 => "i64",
        TokenKind::F32 => "f32",
        TokenKind::F64 => "f64",
        TokenKind::Bool => "bool",
        TokenKind::F32x4 => "f32x4",
        TokenKind::I32x4 => "i32x4",
        TokenKind::F32x8 => "f32x8",
        TokenKind::I32x8 => "i32x8",
        TokenKind::Splat => "splat",
        TokenKind::Identifier => "identifier",
        TokenKind::FloatLiteral => "float",
        TokenKind::IntLiteral => "integer",
        TokenKind::StringLiteral => "string",
        TokenKind::LeftParen => "(",
        TokenKind::RightParen => ")",
        TokenKind::LeftBrace => "{",
        TokenKind::RightBrace => "}",
        TokenKind::Comma => ",",
        TokenKind::Colon => ":",
        TokenKind::Arrow => "->",
        TokenKind::Plus => "+",
        TokenKind::Minus => "-",
        TokenKind::Star => "*",
        TokenKind::Slash => "/",
        TokenKind::Percent => "%",
        TokenKind::Equals => "=",
        TokenKind::LessEqual => "<=",
        TokenKind::GreaterEqual => ">=",
        TokenKind::EqualEqual => "==",
        TokenKind::BangEqual => "!=",
        TokenKind::Less => "<",
        TokenKind::Greater => ">",
        TokenKind::PlusDot => ".+",
        TokenKind::MinusDot => ".-",
        TokenKind::StarDot => ".*",
        TokenKind::SlashDot => "./",
        TokenKind::LessEqualDot => ".<=",
        TokenKind::GreaterEqualDot => ".>=",
        TokenKind::EqualEqualDot => ".==",
        TokenKind::BangEqualDot => ".!=",
        TokenKind::LessDot => ".<",
        TokenKind::GreaterDot => ".>",
        TokenKind::Dot => ".",
        TokenKind::AmpAmp => "&&",
        TokenKind::PipePipe => "||",
        TokenKind::Bang => "!",
        TokenKind::LeftBracket => "[",
        TokenKind::RightBracket => "]",
        TokenKind::Newline => "newline",
        TokenKind::LineComment => "comment",
    }
}
